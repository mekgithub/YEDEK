package com;

public class LompLuppercaseLetters {
    public static void main(String[] args) {
        //All upper case letters
        for(int i=65;i<=90; i ++){
            System.out.println((char)i);
        }
        for(int j=97; j<=122 ; j++){
            System.out.println((char)j);
        }
    }
}
